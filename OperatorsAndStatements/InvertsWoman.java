

public class InvertsWoman{
	
	final public static void main(String... args){
		boolean x = false;
		System.out.println(x);
		x = !x;
		System.out.println(x);

		System.out.println("********************");
		double xx = 1.21;
		System.out.println(xx);
		xx = -xx;
		System.out.println(xx);
		xx = -xx;
		System.out.println(xx);
	}


}